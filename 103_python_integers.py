# numerical types
# integers and Float, complex numbers, big ints

# integers
# full numbers
print(10)
print(type(10))

# floats
# decimal numbers
print(10.0)
print(type(10.0))

# they can be used together
print(10/3)

# Add
print(10 + 3)

# Subtract
print(10 - 3)

# divide
print(10 / 3)

# multiply
print(10 * 3)

# modulo %
print(10 % 3)