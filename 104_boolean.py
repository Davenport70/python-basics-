# Comparison Operators
# Comparison operators allows to evalute a code
# they output --> 'bool'
num_a = 10
num_b = '10'
# asserting if something is equal
# use double equal to equal something

print(10 == '10')

# greater than

print('using >')
print(num_a > 10)
print(num_b > 10)

# smaller than
print('using <')
print(num_a < 10)
print(num_b < 10)

# greater than or equal
print('using >=')
print(num_a >= 10)
print(num_b >= 10)

# less than or equal
print('using <=')
print(num_a <= 10)
print(num_b <= 10)

# not than
print('using !=')
print(num_a != 10)
print(num_b != 10)

# is something
print(0 == 0)
